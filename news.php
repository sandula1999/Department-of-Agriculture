                <h3>Yala harvest in crisis due to non-availability of fertilizer</h3>
                <p>The Ministry of Agriculture states that paddy cultivation in the Yala season is facing a serious crisis.</p>

                <img src="Images/newss3.png" alt="img-3">
                <p>Accordingly, a senior official of the Ministry stated that the crisis situation has arisen due to the non-avalibility of the required stocks of fertilizer for the Yala season.</p>
                <p>The official pointed out that paddy cultivation in the Yala season will fail if stocks of fertilizer are not received by the country before 15th July</p>
                <p>He further stated that if the required stocks of fertilizer are received before 15th of June, a successful harvest of at least 50% of the current cultivation can be expected as the result.</p>
                <p>Yala season harvest would be in serious be crisis if the date is exceeded, he emphasized.
                    Meanwhile, the Ministry of Agriculture states that no funds has been received so far to pay compensation for crop damage during the past Maha season.</p>
                <p>The Ministry stated that it had requested Rs. 15 billion for compensation to be approved by the cabinet as compensation, and added that no final decision has been taken as to whether compensation will be paid for crop damage during the Yala season.</p>
            
